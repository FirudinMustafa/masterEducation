/**
 * Mobile drawer — AdminMobileHeader içindeki ☰ butonuna bağlı. Tüm nav item'leri
 * drawer'da, link tıklanınca otomatik kapanır + body scroll-lock.
 */
export function AdminMobileDrawer({
  open,
  onClose,
}: {
  open: boolean;
  onClose: () => void;
}) {
  useEffect(() => {
    document.body.style.overflow = open ? "hidden" : "";
    return () => {
      document.body.style.overflow = "";
    };
  }, [open]);

  if (!open) return null;
  return (
    <div className="fixed inset-0 z-[60] bg-black/40 md:hidden" onClick={onClose}>
      <aside
        onClick={(e) => e.stopPropagation()}
        className="absolute left-0 top-0 flex h-full w-[min(280px,82vw)] flex-col bg-white shadow-2xl"
      >
        <div className="flex items-center justify-between border-b border-gray-100 p-3">
          <span className="text-sm font-bold text-brand-black">Yonetim Menu</span>
          <button
            onClick={onClose}
            aria-label="Kapat"
            className="rounded-full p-2 hover:bg-gray-100 cursor-pointer"
          >
            <XMarkIcon className="h-5 w-5" />
          </button>
        </div>
        <AdminNavBody onNavigate={onClose} />
      </aside>
    </div>
  );
}

