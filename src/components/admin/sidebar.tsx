/**
 * State container — drawer + button + state'i tek client comp'ta tutar; mobile
 * header bunu render eder.
 */
export function AdminMobileNavTrigger() {
  const [open, setOpen] = useState(false);
  return (
    <>
      <AdminMobileMenuButton onOpen={() => setOpen(true)} />
      <AdminMobileDrawer open={open} onClose={() => setOpen(false)} />
    </>
  );
}
